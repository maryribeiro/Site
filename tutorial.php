                <td>1° Passo</td>
                <td>
                    <p>A maior placa de circuito que você verá é a placa-mãe. Antes de limpá-la, é preciso retirar todos os componentes.
                        <br/>
                        Comece pelo cooler do processador; use uma chave de fenda para girar as quatro travas presentes, girando-as em sentido anti-horário.
                        <br/>
                        Depois puxe as travas para cima até ouvir um estalo, o que indicará que o cooler soltou-se da placa-mãe.
                        <br/>
                        Por fim, desconecte o cabo de energia que liga o componente à placa;</p>
                </td>
                <td><img src="img/cooler.jpg" width="300" height="250"></td>
            </tr>
            <tr>
                <td>2° Passo</td>
                <td>
                    <p>Desconecte todos os fios que estiverem ligados à placa-mãe.</p>
                    <p>Uma dica é fotografar cada conexão antes de retirá-la, o que lhe ajudará a saber a posição de cada fio na hora de reconectar os cabos;</p>
                    <br/>
                </td>
                <td><img src="img/fio.jpg" width="300" height="200"></td>
            </tr>
            <tr>
                <td>3°Passo</td>
                <td>
                    <p id="texto">Se o seu gabinete for pequeno, talvez você precise tirar componentes como HD e fonte de alimentação para ter acesso a toda área da placa-mãe.</p>
                    <p id="texto">Realize o mesmo procedimento descrito acima, tirando fotos dos cabos para facilitar ao reconectá-los.</p>
                    <p id="texto">Para melhor identificar, você pode escrever códigos e criar uma legenda em papel;</p>
                    <br>
                </td>
                <td><img src="img/fonte.jpg" width="300" height="200"></td>
            </tr>
            <tr>
                <td>4° Passo</td>
                <td>
                    <p id="texto">Agora retire a fonte de alimentação. Para isso, use a chave philips para tirar os parafusos que prendem a fonte ao gabinete.</p>
                    <p id="texto">Segure a fonte antes de soltar o último parafuso para que não caia sobre a placa-mãe, estragando algum componente.</p>
                    <p id="texto">depois puxe a peça para desconectá-la da placa-mãe;</p>
                </td>
                <td><img src="img/fonte 2.jpg" width="300" height="200"></td>
            </tr>
            <tr>
                <td>5° Passo</td>
                <td>
                    <p id="texto">Com um pincel de cerdas longas seco, comece retirando toda a poeira da placa-mãe.</p>
                    <p id="texto">Faça movimentos delicados e não esfregue com força, pois isso pode danificar os circuitos;</p>
                </td>
                <td><img src="img/limpando.jpg" width="300" height="200"></td>
            </tr>
            <tr>
            <td>6° Passo</td>
                <td>
                    <p id="texto">Ao final deste período, dê um jato de limpa-contato sobre toda a placa-mãe.</p>
                    <p id="texto">O produto, que é destinado a este fim, recupera as partes oxidadas dos contatos;</p>
                    </td>
                    <td><img src="img/8.jpg" width="300" height="200"></td>
            </tr>
            <tr>
            <td>7° Passo</td>
                <td>
                    <p id="text">Agora a placa-mãe está completamente limpa, mas é preciso esperar que o limpa-contato fique seco.</p>
                    <p id="text">Aguarde cerca de 30 minutos para garantir que o produto evapore;</p>
                    </td>
                    <td><img src="img/9.jpg" width="300" height="200"></td>
            </tr>
            <tr>  
              <td>8° Passo</td>
                <td>
                    <p id="text"> Por fim, feche o gabinete. Sua placa-mãe estará limpa e terá maior vida útil.</p>

                        <br/>
                        Assim, as informações processadas passam sempre pela peça,o que aponta para a importância da limpeza e conservação para um bom desempenho. 
                        <br/>
                        O acúmulo de poeira dentro do gabinete pode impedir a comunicação dos circuitos, ocasionando travamentos, desligamentos e até o dano permanente de algum componente.
                        <br/>

    <h2>Passo a passo de como limpar o seu computador</h2>
        <br>
        <p>Passo 1. A maior placa de circuito que você verá é a placa-mãe. Antes de limpá-la, é preciso retirar todos os componentes.
        <br>
         Comece pelo cooler do processador; use uma chave de fenda para girar as quatro travas presentes, girando-as em sentido anti-horário.
        <br>
        Depois puxe as travas para cima até ouvir um estalo, o que indicará que o cooler soltou-se da placa-mãe.
        <br>
        Por fim, desconecte o cabo de energia que liga o componente à placa;</p>
       <br>
       </div>
       <div class="img">
            <center> <img src="img/cooler.jpg" width="300" height="250"></center>
        </div>
        <div class="img">
            <center> <img src="img/2.jpg" width="300" height="250"></center>
        </div>
       <br>
       <p>Passo 2. Desconecte todos os fios que estiverem ligados à placa-mãe.
           <br>
           Uma dica é fotografar cada conexão antes de retirá-la, o que lhe ajudará a saber a posição de cada fio na hora de reconectar os cabos;</p>
       <br>
       <div class="img">
            <center> <img src="img/fio.jpg" width="300" height="200"></center>
        </div>
       <br>
       <p>Passo 3. Se o seu gabinete for pequeno, talvez você precise tirar componentes como HD e fonte de alimentação para ter acesso a toda área da placa-mãe.
           <br>
           Realize o mesmo procedimento descrito acima, tirando fotos dos cabos para facilitar ao reconectá-los.
           <br>
           Para melhor identificar, você pode escrever códigos e criar uma legenda em papel;</p>
       <br>
       <div class="img">
            <center> <img src="img/fonte.jpg" width="300" height="200"></center>
        </div>
       <br>
       <p>Passo 4. Agora retire a fonte de alimentação. Para isso, use a chave philips para tirar os parafusos que prendem a fonte ao gabinete.
           <br>
           Segure a fonte antes de soltar o último parafuso para que não caia sobre a placa-mãe, estragando algum componente.
           <br>
       depois puxe a peça para desconectá-la da placa-mãe;</p>
